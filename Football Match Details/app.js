function closeTab() {
  getDiv.style.display = "none";
  var teamDetail = document.getElementById("detail-box");
  var childToRemove =  teamDetail.childNodes;
  for(var x=1; x<childToRemove.length; x++){
    teamDetail.removeChild(childToRemove[x]);
    
  }
}